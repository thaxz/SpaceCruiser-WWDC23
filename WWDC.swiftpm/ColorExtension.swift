import Foundation
import SwiftUI

extension Color {
    static let theme = ColorTheme()
}

struct ColorTheme {
    
    let mediumPurple = Color("mediumPurple")
    let darkerPurple = Color("darkerPurple")
    let mediumBlue = Color("mediumBlue")
    let darkerBlue = Color("darkerBlue")
    let darkerGray = Color("darkerGray")
}

